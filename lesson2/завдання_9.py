#Знайти суму елементів масиву, розташованих між мінімальним імаксимальним елементами.
l = []
a = int(input("Скільки елементів Ви бажаєте мати в списку?\n"))
if a == 0:
    print("На жаль дія не можлива(")
else:
    for i in range(a):
        b = int(input(f"Введіть елемент №{i+1}:"))
        if b not in l:
            l.append(b)
    l.remove(max(l))
    l.remove(min(l))
    s = sum(l)    
    print("Сума елементів між мінімальним і максимальним:", s)